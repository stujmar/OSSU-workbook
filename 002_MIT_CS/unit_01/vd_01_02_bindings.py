# Bindings

# Variables
# Often called bindings

# Python has a special feature called a binding.
# A binding is a connection between a variable and a value.
x = 1

# Python strings are ordered and indexed
y = "two"

# Python floats are not integers
z = 3.14

# Python lists are mutable
my_list = [1, 2, 3]

# Python dictionaries are like hashes in Perl
my_dict = {'one': 1, 'two': 2, 'three': 3}

# Python tuples are immutable
my_tuple = (1, 2, 3)

# Python sets are unordered collections of unique elements
my_set = {1, 2, 3}
